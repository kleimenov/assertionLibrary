
const sumOfNumbers = function(x, y) {
    return x + y;
}

const multOfnumbers = function(x, y) {
    return x * y;
}

let numberOne = 6;
let numberTwo = 6;

console.assert(sumOfNumbers(numberOne, numberTwo) === 12);
